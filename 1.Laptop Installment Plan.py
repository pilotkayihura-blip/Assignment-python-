# Store the total laptop price
total_price =  1200000

# Store the deposit amount
deposit = 300000

# Number of months
months = 6

# Calculate the remaining balance
remaining_balance = total_price - deposit

# Calculate the monthly installment
monthly_installment = remaining_balance / months

# Display the results
print("Total Laptop Price:", total_price, "RWF")
print("Deposit Paid:", deposit, "RWF")
print("Remaining Balance:", remaining_balance, "RWF")
print("Monthly Installment:", monthly_installment, "RWF")